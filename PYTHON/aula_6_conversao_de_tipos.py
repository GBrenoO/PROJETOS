# Conversão de tipos, coerção 
# Type corvertion, typecasting, coercion 
# É o ato de converter um tipo em outro 
# Tipos imutaveis e primitivos:
# str, int, float, bool
print(1 + 1) 
print('a' + 'b')

print ('1', type('1'))
print (int('1'), type(int ('1')))
print ((float('1') + 1), type (float('1') + 1))
print (bool('10'), type(bool('10')))
print (bool(''), type (bool('')))