# Tipos int e float 
# Int -> Numero inteiro
# O tipo int representa qualquer numero 
# Positivo ou negativo. int sem sinal é considerado 
# Positivo
print(11) #int 
print(-11) #int 
print(0) #int 

# Float -> Numero com ponto flutuante
# O tipo float representa qualquer numero 
# Positivo ou negativo com ponto flutuante 
# Float sem sinal é considerado positivo 
print(1.1, 10.11, -1.5) 

# A função type mostr o tipp que o Python 
# Inseriu ao valor  
print(    type('texto entre aspas')     ) # <class 'str'>
print(    type(1)     ) # <class 'int'>
print(    type(1.5)     ) # class 'float'>

